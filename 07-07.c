// Random Pattern 
#include <stdio.h>
#include <stdlib.h>
#include <time.h>
int main()
{
    char size[10][10] = {'+','+','+','+','+','+','+','+','+','+',
                         '+','+','+','+','+','+','+','+','+','+',
                         '+','+','+','+','+','+','+','+','+','+',
                         '+','+','+','+','+','+','+','+','+','+',
                         '+','+','+','+','+','+','+','+','+','+',
                         '+','+','+','+','+','+','+','+','+','+',
                         '+','+','+','+','+','+','+','+','+','+',
                         '+','+','+','+','+','+','+','+','+','+',
                         '+','+','+','+','+','+','+','+','+','+',
                         '+','+','+','+','+','+','+','+','+','+'};
    int num, count = 0;
    int i, j;
    printf("Enter size >> ");
    scanf("%d", &num);
    srand(time(0));

    while(count < num){
        int y = rand() % 10;
        int x = rand() % 10;
        if(size[x][y] == '+'){
            size[x][y] = '@';
            count++;
        }
    }

    for(i = 0; i < 10; i++){
        for(j = 0; j < 10; j++){
            printf("%c", size[i][j]);
        }
        printf("\n");
    }
    return 0;
}