#include <stdio.h>

int main(){
    int a, b, i, j;
    scanf("%d", &a);
    b = a;

    for(i = 0; i<a; i++){
        for(j = 0; j<a-i-1; j++) printf(" ");
        for(j = 0; j<a+2*i; j++) printf("*");
        printf("\n");
    }
    for(i = 0; i<a-1; i++){
        for(j = 0; j<3*a-2; j++) printf("*");
        printf("\n");
    }
    for(i = 0; i<a-1; i++){
        for(j = 0 ; j < i+1; j++) printf(" ");
        for(j = 0 ; j < 3*a-i*2-4; j++) printf("*");
        printf("\n");
    }

    return 0;
}

